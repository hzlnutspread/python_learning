import math

# we import the math module

pi = 3.14
x = 52
y = 2
z = 0.115

print(round(pi))  # rounds to the nearest full integer

print(math.ceil(pi))  # rounds UP to the nearest full integer
print(math.floor(pi))  # rounds DOWN to the nearest full integer

print(abs(pi))  # gives the absolute value of a number

print(pow(pi, 3))  # raises pi to the power of 2

print(math.sqrt(pi))  # finds square root of number

print(max(pi, x, y, z))

print(min(pi, x, y, z))
