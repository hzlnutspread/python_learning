# type casting converts data type of a value to another data type

x = 1  # int
y = 2.0  # float
z = "3"  # string

# if we want to convert y to an integer we would do the following:
y = int(y)

print(x)
print(y)
print(z)

# or we would do this:
print(x)
print(int(y))
print(z)

# so to convert z into an integer we would do this:
z = int(z)

# to convert x to a float
x = (float(x))
print(x)
