name = "Sniper"

print(len(name))  # shows length of the string

print(name.find("i"))  # shows position of character

print(name.capitalize())  # capitalises the first letter

print(name.upper())  # capitalises all
print(name.lower())  # makes all letters lower case

print(name.isdigit())  # is the string a digit?

print(name.isalpha())  # is the string only alphabetical letters?

print(name.count("i"))  # how many of this character are there?

print(name.replace("i", "ee"))  # replaces the first string with the second string

print(name*3)
